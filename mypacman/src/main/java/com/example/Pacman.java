    @Override
    public void start (Stage primaryStage) {
        primaryStage.setTitle("Pac-Man Game");

        Canvas canvas = new Canvas(WIDTH, HEIGHT);
        GraphicsContext gc = canvas.getGraphicsContext2D();

        StackPane root = new StackPane();
        root.getChildren().add(canvas);

        Scene scene = new Scene(root, WIDTH, HEIGHT);
        scene.setOnKeyPressed(e -> handleKeyPress(e.getCode()));
        scene.setOnKeyReleased(e -> handleKeyRelease(e.getCode()));

        primaryStage.setScene(scene);
        primaryStage.show();

        new AnimationTimer() {
            @Override
            public void handle(long now) {
                update();
                render(gc);
            }
        }.start();
    }

    private void update() {
        if(movingUp && canMove(pacManX, pacManY-pacManSpeed)) {
            pacManY -= pacManSpeed;
        }
        if (movingDown && canMove(pacManX, pacManY + pacManSpeed)) {
            pacManY += pacManSpeed;
        }
        if (movingLeft && canMove(pacManX - pacManSpeed, pacManY)) {
            pacManX -= pacManSpeed;
        }
        if (movingRight && canMove(pacManX + pacManSpeed, pacManY)) {
            pacManX += pacManSpeed;
        }
    }

    private boolean canMove(int x, int y) {
        int tileX = (int)(x/TILE_SIZE);
        int tileY = (int)(y/TILE_SIZE);

        // Check if the next position is within the maze bounds and not a wall
        return tileX >= 0 && tileX < maze[0].length && tileY >= 0 && tileY < maze.length && maze[tileY][tileX] != 1;
    }

    private void render(GraphicsContext gc) {
        gc.clearRect(0, 0, WIDTH, HEIGHT);

        // Draw Walls
        for (int row = 0; row < maze.length; row++) {
            for (int col = 0; col < maze[row].length; col++) {
                if (maze[row][col] == 1) {
                    gc.setFill(Color.BLUE);
                    gc.fillRect(col*TILE_SIZE, row*TILE_SIZE, TILE_SIZE, TILE_SIZE);
                } else if (maze[row][col] == 0 || maze[row][col] == 2) {
                    gc.setFill(Color.BLACK);
                    gc.fillRect(col*TILE_SIZE, row*TILE_SIZE, TILE_SIZE, TILE_SIZE);
                }
            }
        }

        // Draw Pac-Man
        gc.setFill(Color.YELLOW);
        gc.fillOval(pacManX, pacManY, TILE_SIZE, TILE_SIZE);
    }

    private void handleKeyPress(KeyCode code) {
        switch (code) {
            case UP:
                movingUp = true;
                break;
            case DOWN:
                movingDown = true;
                break;
            case LEFT:
                movingLeft = true;
                break;
            case RIGHT:
                movingRight = true;
                break;
            default:
                break;
        }
    }

    private void handleKeyRelease(KeyCode code) {
        switch (code) {
            case UP:
                movingUp = false;
                break;
            case DOWN:
                movingDown = false;
                break;
            case LEFT:
                movingLeft = false;
                break;
            case RIGHT:
                movingRight = false;
                break;
            default:
                break;
        }
    }
